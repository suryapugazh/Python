import math

#Square root
print(math.sqrt(4))

# Whole or Round
print(math.ceil(1.55))     # It'll round to Greatest Whole Number.
print(math.floor(1.55))    # It'll remove point numbers after the point and make it as round or Smallest Whole Number.

# Factorial
print(math.factorial(4))

# Float Absolute Value | fabs(), if the value is in Minus or Negative (-1) then it will return it as Plus or Positive Number (1).
print(math.fabs(5))     
print(math.fabs(-5))

# Power or Exponential
print(math.pow(3,5))

# Logarithm | log(), is the inverse function of exponentiation
print(math.log2(2))     # Log by base 2
print(math.log10(15))   # Log by base 10

# Constant Value
print(math.pi)     # Value of Pi (Ï€)
print(math.e)      # Value of e  (e)