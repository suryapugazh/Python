a = 1
b = 2

print(a + b) # Addition
print(a - b) # Subtraction
print(a / b) # Divition        - Divide with point values
print(a // b) # Floor Divition - Divide without point values
print(a % b) #  Modulus        - Remainder value of Division
print(a * b) # Multiplication
print(a ** b) # Exponential    - power value
