a = 2     # Assignment
print(a)

a += 2     # Arithmetic Assignment - Additon
print(a)

a -= 3     # Arithmetic Assignment - Subtraction
print(a)

a *= 4     # Arithmetic Assignment - Multiplication
print(a)

a **= 5     # Arithmetic Assignment - Exponential
print(a)

a /= 5     # Arithmetic Assignment - Division
print(a)

a //= 5     # Arithmetic Assignment - Floor Divison(Rounded)
print(a)

a %= 5     # Arithmetic Assignment - Modulus(Remainder)
print(a)
