s = "Surya"
print(s)

#String Slice [Array Slicing]

'''
S   a   m   p   l   e
0   1   2   3   4   5
-6  -5  -4  -3  -2  -1
'''

print(s[0:2]) # [start:stop]
print(s[:3])
print(s[1:])
print(s[-1])
print(s[-4:-2]) # -4 ==> start, -2 ==> end i.e. ends with after -2 which is Minus ONe(-1) 

#Reverse String

print(s[::-1]) # [start:stop:step]